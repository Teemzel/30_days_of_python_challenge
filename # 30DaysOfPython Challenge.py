# Checking Data Type

print(type(5))                  # Int
print(type(4.5))                # Float
print(type(True))               #Bool
print(type("Annaconda"))        # String
print(type([1,2,3,8]))          # List 
print(type({3,5.7}))            # Set
print(type((5,8,9,0)))          # Tuple
print(type(6 > 7))              # Bool
print(type({"Language : python"})) # Set
print(type({"Language" : "Java"})) # Dictionary
